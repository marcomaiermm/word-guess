CREATE TABLE IF NOT EXISTS Game (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  uuid TEXT NOT NULL UNIQUE,
  rows INTEGER NOT NULL,
  symbols TEXT NOT NULL,
  word_id INTEGER NOT NULL,
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS Word (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  word TEXT NOT NULL UNIQUE,
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP
);

-- Index for Game table
CREATE INDEX IF NOT EXISTS idx_game_uuid ON Game (uuid);
CREATE INDEX IF NOT EXISTS idx_game_word_id ON Game (word_id);

-- Index for Word table
CREATE INDEX IF NOT EXISTS idx_word_word ON Word (word)
